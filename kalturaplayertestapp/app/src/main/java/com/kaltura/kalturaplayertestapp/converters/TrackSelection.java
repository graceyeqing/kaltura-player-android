package com.kaltura.kalturaplayertestapp.converters;


public class TrackSelection {
    private String textSelectionMode;
    private String textSelectionLanguage;
    private String audioSelectionMode;
    private String audioSelectionLanguage;

    public String getTextSelectionMode() {
        return textSelectionMode;
    }

    public void setTextSelectionMode(String textSelectionMode) {
        this.textSelectionMode = textSelectionMode;
    }

    public String getTextSelectionLanguage() {
        return textSelectionLanguage;
    }

    public void setTextSelectionLanguage(String textSelectionLanguage) {
        this.textSelectionLanguage = textSelectionLanguage;
    }

    public String getAudioSelectionMode() {
        return audioSelectionMode;
    }

    public void setAudioSelectionMode(String audioSelectionMode) {
        this.audioSelectionMode = audioSelectionMode;
    }

    public String getAudioSelectionLanguage() {
        return audioSelectionLanguage;
    }

    public void setAudioSelectionLanguage(String audioSelectionLanguage) {
        this.audioSelectionLanguage = audioSelectionLanguage;
    }
}
